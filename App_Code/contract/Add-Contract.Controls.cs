        protected virtual void AddNewRecords()
        {
          
            ArrayList newRecordList = new ArrayList();
          
            System.Collections.Generic.List<Hashtable> newUIDataList = new System.Collections.Generic.List<Hashtable>();
            // Loop though all the record controls and if the record control
            // does not have a unique record id set, then create a record
            // and add to the list.
            if (!this.ResetData)
            {
              System.Web.UI.WebControls.Repeater rep = (System.Web.UI.WebControls.Repeater)(BaseClasses.Utils.MiscUtils.FindControlRecursively(this, "BudgetTableControlRepeater"));
              if (rep == null){return;}
              
                foreach (System.Web.UI.WebControls.RepeaterItem repItem in rep.Items)
                {
                
                // Loop through all rows in the table, set its DataSource and call DataBind().
                BudgetTableControlRow recControl = (BudgetTableControlRow)(repItem.FindControl("BudgetTableControlRow"));
      
            if (recControl.Visible && recControl.IsNewRecord) {
      BudgetRecord rec = new BudgetRecord();
        
                        if (recControl.budget_amount.Text != "") {
                            rec.Parse(recControl.budget_amount.Text, BudgetTable.budget_amount);
                  }
                
                        if (MiscUtils.IsValueSelected(recControl.budget_created_by)) {
                            rec.Parse(recControl.budget_created_by.SelectedItem.Value, BudgetTable.budget_created_by);
                        }
                        if (MiscUtils.IsValueSelected(recControl.custno)) {
                            rec.Parse(recControl.custno.SelectedItem.Value, BudgetTable.custno);
                        }
              newUIDataList.Add(recControl.PreservedUIData());
              newRecordList.Add(rec);
            }
          }
        }
    
            // Add any new record to the list.
            for (int count = 1; count <= this.AddNewRecord; count++) {
              
                newRecordList.Insert(0, new BudgetRecord());
                newUIDataList.Insert(0, new Hashtable());
              
            }
            this.AddNewRecord = 0;

            // Finally, add any new records to the DataSource.
            if (newRecordList.Count > 0) {
              
                ArrayList finalList = new ArrayList(this.DataSource);
                finalList.InsertRange(0, newRecordList);

                Type myrec = typeof(OEMConversion.Business.BudgetRecord);
                this.DataSource = (BudgetRecord[])(finalList.ToArray(myrec));
              
            }
            
            // Add the existing UI data to this hash table
            if (newUIDataList.Count > 0)
                this.UIData.InsertRange(0, newUIDataList);
        }

        
