        {
          
            ArrayList newRecordList = new ArrayList();
          
            System.Collections.Generic.List<Hashtable> newUIDataList = new System.Collections.Generic.List<Hashtable>();
            // Loop though all the record controls and if the record control
            // does not have a unique record id set, then create a record
            // and add to the list.
            if (!this.ResetData)
            {
              System.Web.UI.WebControls.Repeater rep = (System.Web.UI.WebControls.Repeater)(BaseClasses.Utils.MiscUtils.FindControlRecursively(this, "Contractor_contact_rateTableControlRepeater"));
              if (rep == null){return;}
              
                foreach (System.Web.UI.WebControls.RepeaterItem repItem in rep.Items)
                {
                
                // Loop through all rows in the table, set its DataSource and call DataBind().
                Contractor_contact_rateTableControlRow recControl = (Contractor_contact_rateTableControlRow)(repItem.FindControl("Contractor_contact_rateTableControlRow"));
      
            if (recControl.Visible && recControl.IsNewRecord) {
      Contractor_contact_rateRecord rec = new Contractor_contact_rateRecord();
        
                        if (recControl.end_date.Text != "") {
                            rec.Parse(recControl.end_date.Text, Contractor_contact_rateTable.end_date);
                  }
                
                        if (recControl.rate.Text != "") {
                            rec.Parse(recControl.rate.Text, Contractor_contact_rateTable.rate);
                  }
                
                        if (recControl.start_date.Text != "") {
                            rec.Parse(recControl.start_date.Text, Contractor_contact_rateTable.start_date);
                  }
                
              newUIDataList.Add(recControl.PreservedUIData());
              newRecordList.Add(rec);
            }
          }
        }
    
            // Add any new record to the list.
            for (int count = 1; count <= this.AddNewRecord; count++) {
              
                newRecordList.Insert(0, new Contractor_contact_rateRecord());
                newUIDataList.Insert(0, new Hashtable());
              
            }
            this.AddNewRecord = 0;

            // Finally, add any new records to the DataSource.
            if (newRecordList.Count > 0) {
              
                ArrayList finalList = new ArrayList(this.DataSource);
                finalList.InsertRange(0, newRecordList);

                Type myrec = typeof(OEMConversion.Business.Contractor_contact_rateRecord);
                this.DataSource = (Contractor_contact_rateRecord[])(finalList.ToArray(myrec));
              
            }
            
            // Add the existing UI data to this hash table
            if (newUIDataList.Count > 0)
                this.UIData.InsertRange(0, newUIDataList);
        }

        
