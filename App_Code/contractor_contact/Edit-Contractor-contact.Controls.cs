        public virtual WhereClause CreateWhereClause()
        {
            // This CreateWhereClause is used for loading the data.
            Contractor_contact_attachTable.Instance.InnerFilter = null;
            WhereClause wc = new WhereClause();
    
            // CreateWhereClause() Compose the WHERE clause consist of:
            // 1. Static clause defined at design time.
            // 2. User selected search criteria.
            // 3. User selected filter criteria.
            
        
            if (MiscUtils.IsValueSelected(this.Contractor_contact_attachSearchText)) {
                if (this.Contractor_contact_attachSearchText.Text == BaseClasses.Resources.AppResources.GetResourceValue("Txt:SearchForEllipsis", null) ) {
                        this.Contractor_contact_attachSearchText.Text = "";
                } else {
                  // Strip "..." from begin and ending of the search text, otherwise the search will return 0 values as in database "..." is not stored.
                  if (this.Contractor_contact_attachSearchText.Text.StartsWith("...")) {
                      this.Contractor_contact_attachSearchText.Text = this.Contractor_contact_attachSearchText.Text.Substring(3,this.Contractor_contact_attachSearchText.Text.Length-3);
                  }
                  if (this.Contractor_contact_attachSearchText.Text.EndsWith("...")) {
                      this.Contractor_contact_attachSearchText.Text = this.Contractor_contact_attachSearchText.Text.Substring(0,this.Contractor_contact_attachSearchText.Text.Length-3);
                      // Strip the last word as well as it is likely only a partial word
                      int endindex = this.Contractor_contact_attachSearchText.Text.Length - 1;
                      while (!Char.IsWhiteSpace(Contractor_contact_attachSearchText.Text[endindex]) && endindex > 0) {
                          endindex--;
                      }
                      if (endindex > 0) {
                          this.Contractor_contact_attachSearchText.Text = this.Contractor_contact_attachSearchText.Text.Substring(0, endindex);
                      }
                  }
                }
                string formatedSearchText = MiscUtils.GetSelectedValue(this.Contractor_contact_attachSearchText, this.GetFromSession(this.Contractor_contact_attachSearchText));
                // After stripping "..." see if the search text is null or empty.
                if (MiscUtils.IsValueSelected(this.Contractor_contact_attachSearchText)) {
                      
                    // These clauses are added depending on operator and fields selected in Control's property page, bindings tab.
                  
                    WhereClause search = new WhereClause();
                    
        ColumnList cols = new ColumnList();
      
      cols.Add(Contractor_contact_attachTable.attachment_name, true);
      
      foreach(BaseColumn col in cols)
      {
      
          search.iOR(col, BaseFilter.ComparisonOperator.Contains, MiscUtils.GetSelectedValue(this.Contractor_contact_attachSearchText, this.GetFromSession(this.Contractor_contact_attachSearchText)), true, false);
        
      }
    
                    wc.iAND(search);
                  
                }
            }
                  
            if (MiscUtils.IsValueSelected(this.contractor_contact_idFilter2)) {
                        
                int selectedItemCount = 0;
                foreach (ListItem item in this.contractor_contact_idFilter2.Items){
                    if (item.Selected) {
                        selectedItemCount += 1;
                        
                          
                    }
                }
                WhereClause filter = new WhereClause();
                foreach (ListItem item in this.contractor_contact_idFilter2.Items){
                    if ((item.Selected) && ((item.Value == "--ANY--") || (item.Value == "--PLEASE_SELECT--")) && (selectedItemCount > 1)){
                        item.Selected = false;
                    }
                    if (item.Selected){
                        filter.iOR(Contractor_contact_attachTable.contractor_contact_id, BaseFilter.ComparisonOperator.EqualsTo, item.Value, false, false);
                    }
                }
                wc.iAND(filter);
                    
            }
                           
            return wc;
        }
        
         
